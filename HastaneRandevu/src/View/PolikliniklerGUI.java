    private void btnPoliklinikGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPoliklinikGuncelleActionPerformed
        if(tfPoliklinikAdi_pd.getText() == null){
            Helper.showMessage("fill");
        }else{
            Poliklinik edittedPoliklinik = new Poliklinik();
            edittedPoliklinik.setPoliklinikAdi(tfPoliklinikAdi_pd.getText());
            edittedPoliklinik.setPoliklinikID(selectedPoliklinik.getPoliklinikID());
            if(poliklinikIslemleri.Guncelle(edittedPoliklinik)){
                poliklinikTablo();
                Helper.showMessage("Poliklinik Güncellendi");
            }else{
                Helper.showMessage("HATA! Poliklinik Güncellenemedi.");
            }
        }
        
    }//GEN-LAST:event_btnPoliklinikGuncelleActionPerformed

    private void tblPolikliniklerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPolikliniklerMouseClicked
        int id = ((int)tblPoliklinikler.getValueAt(tblPoliklinikler.getSelectedRow(), 0))-1;
        selectedPoliklinik = poliklinikler.get(id);
        bilgiGetir();
    }//GEN-LAST:event_tblPolikliniklerMouseClicked

    private void btnPoliklinikSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPoliklinikSilActionPerformed
        if(poliklinikIslemleri.Sil(selectedPoliklinik)){
            poliklinikTablo();
            Helper.showMessage("Poliklinik Silindi.");
            selectedPoliklinik = poliklinikler.get(0);
            bilgiGetir();
        }else{
            Helper.showMessage("HATA! Poliklinik Silinemedi.");
        }
    }//GEN-LAST:event_btnPoliklinikSilActionPerformed

    private void btnEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEkleActionPerformed
        if(tfPoliklinikAdi_pe.getText() != null){
            Poliklinik poliklinik = new Poliklinik();
            poliklinik.setPoliklinikAdi(tfPoliklinikAdi_pe.getText());
            if(poliklinikIslemleri.Ekle(poliklinik)){
                poliklinikTablo();
                Helper.showMessage("Poliklinik Eklendi.");
                tfPoliklinikAdi_pe.setText("");
            }else{
                Helper.showMessage("HATA! Poliklinik Eklenemedi.");
            }
        }else{
            Helper.showMessage("fill");
        }
    }//GEN-LAST:event_btnEkleActionPerformed

    private void tfAramaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfAramaKeyReleased
        String ifade = tfArama.getText();
        TableRowSorter<DefaultTableModel>  tableRowSorter = new TableRowSorter<DefaultTableModel>(tableModel);
        tblPoliklinikler.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(RowFilter.regexFilter(ifade));
    }//GEN-LAST:event_tfAramaKeyReleased

