    public void poliklinikTablo(){
    
        tableModel.setRowCount(0);
        poliklinikler = poliklinikIslemleri.Listele("SELECT * FROM poliklinikler");
        int no = 0;
        for(Poliklinik poliklinik : poliklinikler){
            Object[] row = { no + 1 , poliklinik.getPoliklinikAdi()};
            tableModel.addRow(row);
            no++;
        }
        tblPoliklinikler.changeSelection(0, 0, false, false);
        selectedPoliklinik = poliklinikler.get(0);
        bilgiGetir();
        
    }
    
    public void bilgiGetir(){
        tfPoliklinikAdi_pd.setText(selectedPoliklinik.getPoliklinikAdi());
    }
    
    
