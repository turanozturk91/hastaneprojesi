    private void bilgileriGetir(Randevu randevu){
        
        tfHastaAdi.setText(randevu.getHasta().getisimSoyisim());
        tfTCKN.setText(randevu.getHasta().getTckn());
        String stringDogumTarihi = randevu.getHasta().getDogumTarihi();
        SimpleDateFormat formatDT = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date dogumTarihi = formatDT.parse(stringDogumTarihi);
            dateDogumTarihi.setDate(dogumTarihi);
        } catch (ParseException ex) {
            Logger.getLogger(RandevuDuzenleGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(randevu.getHasta().getCinsiyet().equals("E")){
            rbErkek.setSelected(true);
        }else{
            rbKadin.setSelected(true);
        }
        tfTelefon.setText(randevu.getHasta().getTelefon());
        taAdres.setText(randevu.getHasta().getAdres());
        cmbPoliklinik.setSelectedItem(poliklinikIslemleri.idToPoliklinik(randevu.getPoliklinikId()));
        
        ArrayList<Doktor> doktorlar = doktorIslemleri.Listele("SELECT * FROM doktorlar WHERE poliklinik_id = '"+ randevu.getPoliklinikId() +"' ");
        cmbDoktor.removeAllItems();
        for(Doktor doktor : doktorlar){
            cmbDoktor.addItem(doktor.getisimSoyisim());
        }
        cmbDoktor.setSelectedItem(doktorIslemleri.idToDoktor(randevu.getDoktorId()));
        String stringDate = new String();
        stringDate = randevu.getTarih();
        System.out.println(stringDate);
        SimpleDateFormat formatRT = new SimpleDateFormat("dd/MM/yyyy");
        Date randevuTarihi;
        try {
            randevuTarihi = formatRT.parse(stringDate);
            dateChoose.setDate(randevuTarihi);
        } catch (ParseException ex) {
            Logger.getLogger(RandevuDuzenleGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        cmbSaat.removeAllItems();
        ArrayList<String> uygunSaatler = takvimIslemleri.uygunSaatler(randevu.getTarih(), randevu.getDoktorId(), takvimIslemleri.saatToInt(selectedRandevu.getSaat()));
        for(String saat : uygunSaatler){
            cmbSaat.addItem(saat);
        }
        System.out.println("SAAT : " + randevu.getSaat());
        cmbSaat.setSelectedItem(selectedRandevu.getSaat());
        
    }
    
