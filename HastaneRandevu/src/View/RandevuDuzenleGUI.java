    public void randevuTablo(){
    
        tableModel.setRowCount(0);
        randevular = randevuIslemleri.Listele("SELECT * FROM randevular");
        int no = 0;
        for(Randevu randevu : randevular){
           Object[] row = {no+1 , randevu.getHasta().getisimSoyisim(), randevu.getHasta().getTckn(), randevu.getTarih(), randevu.getSaat() , poliklinikIslemleri.idToPoliklinik(randevu.getPoliklinikId()), doktorIslemleri.idToDoktor(randevu.getDoktorId())};
           tableModel.addRow(row);
           no++;
        }
        tblRandevular.changeSelection(0, 0, false, false);
        selectedRandevu = randevular.get(0);
        nesneKopyala(selectedRandevu, edittedRandevu);
        bilgileriGetir(edittedRandevu);
        cmbSaat.setSelectedItem(selectedRandevu.getSaat());
        
    }

