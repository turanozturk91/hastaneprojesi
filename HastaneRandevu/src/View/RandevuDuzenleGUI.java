    private void btnGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuncelleActionPerformed
        String hastaIsimSoyisim = tfHastaAdi.getText();
        String hastaTCKN = tfTCKN.getText();
        String hastaTel = tfTelefon.getText();
        String hastaAdres = taAdres.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        String hastaDogumTarihi = "";
        hastaDogumTarihi = sdf.format(dateDogumTarihi.getDate());
        String hastaCinsiyet = buttonGroup1.getSelection().getActionCommand();
        if( hastaIsimSoyisim.equals("") || (hastaTCKN.equals("")) || (hastaTel.equals("")) || (hastaAdres.equals("")) ){
            Helper.showMessage("fill");
        }else{
            
            if( (edittedRandevu.getDoktorId() == 0) || (edittedRandevu.getPoliklinikId() == 0) || edittedRandevu.getSaat().equals("") || edittedRandevu.getTarih().equals("") ){
               Helper.showMessage("fill");
            }else{
                
                edittedRandevu.getHasta().setisimSoyisim(hastaIsimSoyisim);
                edittedRandevu.getHasta().setTckn(hastaTCKN);
                edittedRandevu.getHasta().setTelefon(hastaTel);
                edittedRandevu.getHasta().setAdres(hastaAdres);
                edittedRandevu.getHasta().setDogumTarihi(hastaDogumTarihi);
                edittedRandevu.getHasta().setCinsiyet(hastaCinsiyet);

                
                if( randevuIslemleri.Guncelle(edittedRandevu) ){
                    
                    randevuTablo();
                    Helper.showMessage("RANDEVU GÜNCELLENDİ.");

                }else{
                    Helper.showMessage("HATA! Randevu Eklenemedi.");
                }
                
            }
            
        }
    }//GEN-LAST:event_btnGuncelleActionPerformed

