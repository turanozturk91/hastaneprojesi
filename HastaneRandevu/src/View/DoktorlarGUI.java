    public void doktorlarTablo(){
        tableModel.setRowCount(0);
        doktorlar = doktorIslemleri.Listele("SELECT * FROM doktorlar");
        if(doktorlar.size() != 0){
            int no = 0;
            for(Doktor doktor : doktorlar){
                Object[] row = { no + 1, doktor.getisimSoyisim(), poliklinikIslemleri.idToPoliklinik(doktor.getPoliklinikId()) };
                tableModel.addRow(row);
                no++;
            }
        }
    }

    public void bilgiGetir(Doktor doktor){
        
        if(doktor != null){
        
            tfIsimSoyisim_dd.setText(doktor.getisimSoyisim());
            tfTCKN_dd.setText(doktor.getTckn());
            SimpleDateFormat format1 = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date dogumTarihi = format1.parse(doktor.getDogumTarihi());
                dateDogumTarihi_dd.setDate(dogumTarihi);
            } catch (ParseException ex) {
                Logger.getLogger(DoktorlarGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            tfDoktorTelefon_dd.setText(doktor.getTelefon());
            if(doktor.getCinsiyet().equals("E")){
                rbErkek_dd.setSelected(true);
            }else{
                rbKadın.setSelected(true);
            }
            taAdres_dd.setText(doktor.getAdres());
            ArrayList<Poliklinik> poliklinikler = poliklinikIslemleri.Listele("SELECT * FROM poliklinikler");
            for(Poliklinik poliklinik : poliklinikler){
                cmbPoliklinik_dd.addItem(poliklinik.getPoliklinikAdi());
            }
            cmbPoliklinik_dd.setSelectedItem(poliklinikIslemleri.idToPoliklinik(doktor.getPoliklinikId()));
        
        }
       
    }
    
