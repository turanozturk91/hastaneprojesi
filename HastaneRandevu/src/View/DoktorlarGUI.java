    private void btnGuncelle_ddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuncelle_ddActionPerformed
        
        Boolean isEmpty = (tfIsimSoyisim_dd.getText() == null ) || (tfTCKN_dd.getText() == null) || (btngrpCinsiyet_dd.getSelection().getActionCommand() == null)
                || (cmbPoliklinik_dd.getSelectedItem() == null) || (dateDogumTarihi_dd.getDate() == null) || (taAdres_dd.getText() == null) || (tfDoktorTelefon_dd.getText() == null);
        
        if( isEmpty ){
            Helper.showMessage("fill");
        }else{
            Doktor yeniDoktor = new Doktor();
            yeniDoktor.setId(selectedDoktor.getId());
            yeniDoktor.setisimSoyisim(tfIsimSoyisim_dd.getText());
            yeniDoktor.setTckn(tfTCKN_dd.getText());
            yeniDoktor.setCinsiyet(btngrpCinsiyet_dd.getSelection().getActionCommand());
            yeniDoktor.setPoliklinikId(poliklinikIslemleri.PoliklinikToID(cmbPoliklinik_dd.getSelectedItem().toString()));
            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            String dogumTarihi = format.format(dateDogumTarihi_dd.getDate());
            yeniDoktor.setDogumTarihi(dogumTarihi);
            yeniDoktor.setAdres(taAdres_dd.getText());
            yeniDoktor.setTelefon(tfDoktorTelefon_dd.getText());
            
            if( doktorIslemleri.Guncelle(yeniDoktor) ){
                doktorlarTablo();
                Helper.showMessage("DOKTOR GÜNCELLENDİ.");

            }else{
                Helper.showMessage("HATA! Doktor Eklenemedi.");
            }
            
        }
        
    }//GEN-LAST:event_btnGuncelle_ddActionPerformed

